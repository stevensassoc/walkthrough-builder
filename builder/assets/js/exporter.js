/* Export: turn a project into a downloadable tour ZIP. manifestToTourJs is pure
   and tested; buildTourZip is browser-only (JSZip + fetch). UMD. */
(function (root, factory) {
  var mod = factory(root);
  if (typeof module !== 'undefined' && module.exports) { module.exports = mod; }
  root.Exporter = mod;
})(typeof window !== 'undefined' ? window : globalThis, function (root) {
  'use strict';

  function manifestToTourJs(manifest) {
    return 'window.TOUR = ' + JSON.stringify(manifest, null, 2) + ';\n';
  }

  function readme(slug) {
    return [
      'HOW TO PUBLISH / VIEW THIS WALKTHROUGH',
      '======================================',
      '',
      'This folder is a small WEBSITE, not a document. It must be opened from a',
      'web server (http / https) - NOT by double-clicking index.html.',
      '',
      'Why: modern browsers block local files (file://) from loading images into',
      'the 3D / WebGL view for security, so double-clicking shows the buttons but',
      'the 360 photos stay blank.',
      '',
      'To view or share it:',
      '',
      '1. Publish it (recommended): upload the "' + slug + '" folder to any static',
      '   web host (GitHub Pages, Netlify, etc.). The walkthrough lives at that URL',
      '   and can be shared with anyone. The Builder\'s upcoming Publish feature',
      '   will automate this.',
      '',
      '2. Preview on your computer: open a terminal in this folder and run',
      '       python -m http.server 8000',
      '   then visit  http://localhost:8000/' + slug + '/  in your browser.',
      '',
      'Generated by the Stevens & Associates Walkthrough Builder.',
      ''
    ].join('\n');
  }

  // The Viewer files the export bundles, fetched from the sibling /viewer app.
  var VIEWER_FILES = [
    ['../viewer/index.html', 'index.html'],
    ['../viewer/assets/css/viewer.css', 'assets/css/viewer.css'],
    ['../viewer/assets/js/viewer.js', 'assets/js/viewer.js'],
    ['../viewer/assets/js/pano-engine.js', 'assets/js/pano-engine.js'],
    ['../viewer/assets/js/tour-model.js', 'assets/js/tour-model.js'],
    ['../viewer/assets/js/three.min.js', 'assets/js/three.min.js'],
    ['../viewer/assets/js/screenfull.min.js', 'assets/js/screenfull.min.js']
  ];

  // Collect a tour's files with tour-relative paths. content is a string (text)
  // or a Blob (image). Used by both the ZIP export and the publisher.
  function gatherTourFiles(project, manifest) {
    var files = [{ path: 'tour.js', content: manifestToTourJs(manifest) }];

    var jobs = VIEWER_FILES.map(function (pair) {
      return fetch(pair[0], { cache: 'no-store' }).then(function (r) {
        if (!r.ok) { throw new Error('Could not fetch ' + pair[0] + ' (run from the local server, not file://)'); }
        return r.text();
      }).then(function (text) { files.push({ path: pair[1], content: text }); });
    });

    var logoP = (project.logo
      ? Promise.resolve(project.logo)
      : fetch('../viewer/assets/logo.png', { cache: 'no-store' }).then(function (r) {
          if (!r.ok) { throw new Error('Could not load the default logo'); }
          return r.blob();
        }))
      .then(function (blob) { files.push({ path: 'assets/logo.png', content: blob }); });
    jobs.push(logoP);

    project.scenes.forEach(function (s) {
      files.push({ path: 'assets/panos/' + s.id + '.jpg', content: s.image });
    });

    if (project.cover) { files.push({ path: 'assets/cover.jpg', content: project.cover }); }

    return Promise.all(jobs).then(function () { return files; });
  }

  function buildTourZip(project, manifest) {
    var JSZip = root.JSZip;
    var zip = new JSZip();
    var folder = zip.folder(project.id);
    zip.file('HOW TO PUBLISH THIS TOUR.txt', readme(project.id));
    return gatherTourFiles(project, manifest).then(function (files) {
      files.forEach(function (f) { folder.file(f.path, f.content); });
      return zip.generateAsync({ type: 'blob' });
    });
  }

  return { manifestToTourJs: manifestToTourJs, buildTourZip: buildTourZip, gatherTourFiles: gatherTourFiles, VIEWER_FILES: VIEWER_FILES };
});
